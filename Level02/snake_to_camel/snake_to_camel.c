#include <unistd.h>

int	main(int argc, char **argv)
{
	int	c;

	if (argc == 2)
	{
		c = 0;
		while (argv[1][c] != '\0')
		{
			if (argv[1][c] == '_')
			{
				c++;
				argv[1][c] = argv[1][c] - 32;
			}
			write(1, &argv[1][c], 1);
			c++;
		}
		write(1, "\n", 1);
	}
}
