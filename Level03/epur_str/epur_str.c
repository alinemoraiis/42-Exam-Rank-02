#include <unistd.h>

int	main(int argc, char **argv)
{
	int	c;
	int	a;

	c = 0;
	a = 0;
	if (argc == 2)
	{
		while (argv[1][c] != '\0')
		{
			if (argv[1][c] >= 33 && argv[1][c] <= 126)
			{
				write(1, &argv[1][c], 1);
				a = 0;
			}
			else if (c != 0 && a == 0 && argv[1][c + 1] != '\0')
			{
				write(1, " ", 1);
				a = 1;
			}
			c++;
		}
	}
	write(1, "\n", 1);
}
