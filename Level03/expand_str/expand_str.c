#include <unistd.h>

int	main(int argc, char **argv)
{
	int	c;
	int	a;

	c = 0;
	a = 1;
	if (argc == 2)
	{
		while (argv[1][c] != '\0')
		{
			if (argv[1][c] >= 33 && argv[1][c] <= 126)
			{
				if (a == 0)
				{
					write(1, "   ", 3);
					a = 1;
				}
				write(1, &argv[1][c], 1);
			}
			else if (c != 0)
				a = 0;
			c++;
		}
	}
	write(1, "\n", 1);
}
